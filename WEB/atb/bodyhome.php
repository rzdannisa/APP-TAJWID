<br><h3>Assalamualaikum Wr.Wb.</h3><br>

<p style="font-style: italic;">Al-Quran adalah Kalamullah (firman Allah SWT), merupakan mukjizat yang diturunkan kepada Nabi Muhammad SAW dengan perantara Malaikat Jibril. </p>
Di dalam surah Muzzammil ayat 5, Allah SWT berfirman:<br><br>

<p style="color:#006699;font-weight:bold">"... dan bacalah olehmu Al-Quran ini dengan pelan/tartil (bertajwid)."</p><br>

<b style="color:#FF6699;">Hukum orang yang mempelajari Ilmu Tajwid adalah Fardhu Kifayah</b>. Dan <b style="color:#FF6699;">hukum mengamalkannya adalah Fardhu Ain.</b><br> 
Dan umat Islam yang dapat membaca Al-Quran, 
wajib hukumnya belajar Tajwid, supaya terpelihara huruf, makhraj, ghunnah, dan Mad-nya.<br><br>

Mari kita belajar dan tidak bosan membaca dan menggali isi Al-Quran, serta mengamalkannya.<br>
<b style="color:#FF9966;">"... dan katakanlah: 'Ya Tuhanku, tambahkanlah kepadaku ilmu pengetahuan'." (QS. Thaahaa: 114)</b>.<br>